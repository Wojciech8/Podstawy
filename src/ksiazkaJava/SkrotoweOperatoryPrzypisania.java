package ksiazkaJava;

public class SkrotoweOperatoryPrzypisania {
    public static void main(String[] args) {
        int x = 0;
        x = x +10;
        System.out.println(x);

         // Skrotowy operator += informuje, że zmiennej x należy zwiększyć wartość o 10.
        int y = 0;
        y += 10;
        System.out.println(y);

        //Skrot -= 100 zmniejsza wartość zmiennej z o 100.

        int z = 0;
        z -= 100;
        System.out.println(z);




    }
}
